# 數字運算
x=3*6
print(x)
x=3/6
print(x)
x=3//6
print(x)
x=7//6
print(x)
x=2**3
print(x)
x=2**0.5 # 開根號
print(x)
x=7%3 # 取餘數
print(x)
# 字串運算
s="""Hello 
World"""
print(s)
s="Hello"*3+"world"
print(s)
# 字串會對內部的字元編號(索引)，從 0 開始算起
s="Hello"
print(s[2])
s="Hello"
print(s[1:4]) #包含開頭的編號，不包含結尾
s="Hello"
print(s[1:]) #給開頭不給結尾會先隱藏開頭之後給剩下的字元
s="Hello"
print(s[:4]) #給結尾不給開頭會只隱藏結尾